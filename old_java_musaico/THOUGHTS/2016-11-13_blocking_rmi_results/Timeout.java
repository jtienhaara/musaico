    @Override
    public final No<VALUE> countable ()
        throws ReturnNeverNull.Violation
    {
        return new No<VALUE> ( this.type,
                               this,
                               ValueMustBeCountable.CONTRACT.violation (
                                      this,
                                      this,
                                      this.valueViolation ) );
    }


    /**
     * @see java.lang.Object#equals(java.lang.Object)
     */
    @Override
    public final boolean equals (
                                 Object obj
                                 )
    {
        if ( obj == this )
        {
            return true;
        }
        else if ( obj == null
             || obj.getClass () != this.getClass () )
        {
            return false;
        }

        Timeout<?> that = (Timeout<?>) obj;
        if ( ! this.type.elementClass ().equals (
                   that.type.elementClass () ) )
        {
            return false;
        }

        // Everything matches.
        return true;
    }


